unsigned char SCR_ADC_IsSampling(void)
{
     SCR_SCU_PAGE = 0;
     return  (SCR_ADCOMP_CON & EOCSAMPLE);
}

/*Enable ADC and Trigger a Dummy Conversion  */
void SCR_ADC_EnableModulTriggerDumyConversion(void){
// SCU_PAGE = 0
    SCR_SCU_PAGE = 0;
    SCR_ADCOMP_CON |= (1 << ADCENREQ) ; // Enable ADC
    /* Wait for module to start */
    while (!(SCR_ADC_IsEnabled()))  {}
    // Trigger a dummy conversion to avoid wrong conversion
    SCR_ADCOMP_CON |= (1 << SOC);
    /* wait until dummy conversion is finished */
    while(!(SCR_ADC_IsSampling())){}
}

void SCR_ADC_DisableModule(void){
    SCR_SCU_PAGE = 0;
    SCR_ADCOMP_CON &= ~(1 << ADCENREQ) ; // Disable ADC
}

unsigned char SCR_ADC_IsEnabled(void){
    SCR_SCU_PAGE = 0;
    return(SCR_ADCOMP_CON & ADCENST);
}
unsigned char SCR_ADC_isReadyForNewConversion(void){
    SCR_SCU_PAGE = 0;
    return(SCR_ADCOMP_CON & VF);
}
void SCR_ADC_SelectChannel(SCR_ADC_Chanel_Select channel){
    SCR_SCU_PAGE = 0;
    SCR_ADCOMP_CON |= (channel << ADCCHSEL) ; // select ADC Chanel
}
void SCR_ADC_StartNewConversion(void){
    SCR_SCU_PAGE = 0;
    SCR_ADCOMP_CON |= (1 << SOC);
}
/* Is Conversion available and valid */
unsigned char SCR_ADC_IsResultValid(void)
{
     unsigned char return_char = ADCNORESULTORINVALID;

     SCR_SCU_PAGE = 0;
     if ((SCR_ADCOMP_CON & 0x60) == 0x60)//  in Bit 01100000  VF and EOCSAMPLE
        {
            return_char = ADCRESULTVALID;
        }
     return return_char;
}

unsigned char SCR_ADC_GetResult(void){

    unsigned char return_result=0;
    SCR_SCU_PAGE = 0;
    return_result = SCR_ADCOMP_RES;
    return return_result;
}
unsigned char SCR_ADC_StartNewConversionGetResult(void){

    unsigned char Result=0;
    unsigned char ADC_Timeout = 0;
    SCR_ADC_StartNewConversion();

    while ((SCR_ADC_IsResultValid() != ADCRESULTVALID) && (ADC_Timeout <= 10u))//5microsec Timeout
        {
            Result = SCR_ADC_GetResult();
            ADC_Timeout++;
        }
        return Result;
}
