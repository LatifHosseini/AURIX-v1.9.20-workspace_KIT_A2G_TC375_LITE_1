#define ADCENREQ                0
#define ADCENST                 0x02
#define ADCCHSEL                3
#define SOC                     7
#define EOCSAMPLE               0x40
#define VF                      0x20
#define ADCNORESULTORINVALID    0
#define ADCRESULTVALID          1

